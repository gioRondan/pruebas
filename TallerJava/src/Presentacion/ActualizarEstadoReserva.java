    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean auxbol = true;
        switch (jComboBox1.getSelectedIndex()){
            case 0:
              pp.ICC.actualizarEstadoReserva(Integer.parseInt(Id.getText()), clienteseleccionado, Estado.cancelada);
            break;
            case 1:
              pp.ICC.actualizarEstadoReserva(Integer.parseInt(Id.getText()), clienteseleccionado, Estado.registrada);
            break;
            case 2:
              pp.ICC.actualizarEstadoReserva(Integer.parseInt(Id.getText()), clienteseleccionado, Estado.pagada);
            break;
            case 3:
              pp.ICC.actualizarEstadoReserva(Integer.parseInt(Id.getText()), clienteseleccionado, Estado.facturada);
            break;
            default:
                auxbol = false;
                JOptionPane.showMessageDialog(null, "Error en el indice");
            break;
        }
        if (auxbol) {  
            JOptionPane.showMessageDialog(null, "se ha cambado el estado");
            jComboBox1.setEnabled(false);
            jButton1.setEnabled(false);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed


